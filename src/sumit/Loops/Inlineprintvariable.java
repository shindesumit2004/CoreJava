package sumit.Loops;

public class Inlineprintvariable {

    public static void main(String[] args) {

        for (int i=0; i<20; i++){
            System.out.println( i );
        }
    }

    public static class Arrays {
        public static void main(String[] args) {
            int[] marks = new int[3];
            marks[0] = 97;
            marks[1] = 98;
            marks[2] = 95;
            System.out.println();
        }
    }
}
